        private static async Task NewMethod()
        {
            IIqdbApi api = new IqdbApi.IqdbApi();
            var sw = new Stopwatch();
            sw.Start();


            using (var fs = new FileStream("output.txt", FileMode.Create)) ;
            using (var fs = new FileStream("output_excel.txt", FileMode.Create)) ;

            _urls.Shuffle();
            var counter = 0;
            foreach (var url in _urls)
            {
                try
                {
                    var ex = await api.SearchUrl(url);

                    WriteLog(sw.Elapsed.TotalSeconds, "ok", ex.IsFound.ToString(), url, "", ex.Matches.FirstOrDefault(x => x.MatchType == MatchType.Best)?.Source.ToString());
                    counter = 0;
                }
                catch (HttpRequestException e)
                {
                    WriteLog(sw.Elapsed.TotalSeconds, "er", "Error", url, e.Message);

                    if (e.Message.Contains("403"))
                    {
                        Thread.Sleep(70*60*1000);
                    }

                    counter++;
                    if (counter > 10)
                    {
                        return;
                    }

                    Thread.Sleep(10000);
                }
                catch (Exception e)
                {
                    WriteLog(sw.Elapsed.TotalSeconds, "er", "Error", url, e.Message);
                }
                finally
                {
                    Wait();
                }
            }
        }

